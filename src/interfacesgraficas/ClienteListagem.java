    private void carregarLista(String filtro) {
        try {
            ArrayList<Cliente> clientes = new ClienteDAO().recuperar();
            DefaultTableModel model = (DefaultTableModel) tblLista.getModel();
            //Limpa a tabela 
            model.setNumRows(0);
            for (Cliente aux : clientes) {
                if (filtro == null || getClienteFiltro(filtro, aux)) {
                    String[] saida = new String[3];
                    saida[0] = aux.getCpf();
                    saida[1] = aux.getNome();
                    saida[2] = aux.getTelefone1();
                    //Incluir nova linha na Tabela
                    model.addRow(saida);                    
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }        
    }

    private boolean getClienteFiltro(String filtro, Cliente cliente) {
        filtro = filtro.toUpperCase();
        if (cliente.getNome().toUpperCase().contains(filtro))
            return true;
        if (cliente.getCpf().contains(filtro))
            return true;
        if (cliente.getTelefone1().contains(filtro))
            return true;
        if (cliente.getTelefone2().contains(filtro))
            return true;
        if (cliente.getEmail().toUpperCase().contains(filtro))
            return true;
        return false;
    }

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        try {
            String cpf = (String) tblLista.getModel().getValueAt(tblLista.getSelectedRow(), 0);
            ArrayList<Cliente> clientes = new ClienteDAO().recuperar();
            for (Cliente cliente : clientes) {
                if (cpf.equals(cliente.getCpf())) {
                    this.dispose();
                    new CadCliente(cliente).setVisible(true);
                    break;
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja mesmo excluir", "Exclus√£o de Cliente", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.OK_OPTION) {
                String cpf = (String) tblLista.getModel().getValueAt(tblLista.getSelectedRow(), 0);
                new ClienteDAO().excluir(cpf);
                carregarLista(txtBusca.getText());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

